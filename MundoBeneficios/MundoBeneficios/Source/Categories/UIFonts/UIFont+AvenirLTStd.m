#import "UIFont+AvenirLTStd.h"

@implementation UIFont(AvenirLTStd)

+ (UIFont *)fontAvenirLTStdRoman:(CGFloat)size
{
    return [UIFont fontWithName:@"Avenir-Roman" size:size];
}


+ (UIFont *)fontAvenirLTStdBook:(CGFloat)size
{
    return [UIFont fontWithName:@"Avenir-Book" size:size];
}


+ (UIFont *)fontAvenirLTStdLight:(CGFloat)size
{
    return [UIFont fontWithName:@"Avenir-Light" size:size];
}

@end
